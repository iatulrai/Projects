package headFirst;

public class LoopyFor {
	public static void main(String[] args) {

		System.out.println("Before the loop");

		for (int x = 1; x < 4; x++) {
			System.out.println("In the loop...");

			System.out.println("The valur of x is " + x);

		}

		System.out.println("This is after the loop.");

	}

}
